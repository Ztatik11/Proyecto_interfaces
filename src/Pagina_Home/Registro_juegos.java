    private void registrar_juegoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrar_juegoActionPerformed
        // TODO add your handling code here:
        char [] clave_array = desarrolladora.getPassword();
        String pass = "";

        for (int j = 0; j < clave_array.length; j++) {
            pass+=clave_array[j];
        }
        String [] datos_juego = {titulo.getText(),descripcion.getText(),precio.getText(),nota.getText(),genero.getSelectedItem().toString(),pass};
        String Titulo= "'" + datos_juego[0] + "'";
        try {
            Titulo=conexion_db.leer_resultset_string(conexion_db.realizar_consulta("select Titulo from juegos where Titulo=="+Titulo), Titulo);
        } catch (SQLException ex) {
            Exceptions.printStackTrace(ex);
        }
        if(Titulo==null){
            conexion_db.insertar_una_nueva_fila_en_una_tabla("juegos", "Titulo,Descripcion,Precio,Nota,Genero,Desarrolladora,Numero_jugadores",this.conexion_db.parsear_atributos(datos_juego)+",false" );
        }else{
            //ESTA INTRODUCIDO EN LA BASE DE DATOS
        }
    }//GEN-LAST:event_registrar_juegoActionPerformed

